package com.bridgelabz.top_100_codes;
//Program to Find the Sum of First N Natural Numbers using java
public class SumOfNaturalNum {
        public static void main(String[] args)
        {

            int n = 10;
            int sum = 0;

            for (int i = 1; i <= n; i++)
                sum += i;
            System.out.println(sum);
        }
    }

